const validate = (value: any) => {
  if (typeof value !== "string") {
    throw mongoose.SchemaType.CastError(
      "IPv6",
      `Value is not string: ${value}`
    );
  }

  if (!IPV6_REGEX.test(value)) {
    throw mongoose.SchemaType.CastError(
      "IPv6",
      `Value is not a valid IPv6 address: ${value}`
    );
  }

  return value;
};

/**
 * A field whose value is a IPv6 address: https://en.wikipedia.org/wiki/IPv6.
 */
class IPv6 extends mongoose.SchemaType {
  public static instance = "IPv6";
  constructor(key: string, options: object) {
    super(key, options, "IPv6");
  }

  cast(val: any) {
    validate(val);
    return val;
  }
}

mongoose.Schema.Types.IPv6 = IPv6;

export default IPv6;
